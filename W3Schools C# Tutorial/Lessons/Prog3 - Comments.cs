namespace Prog3___Comments
{
    class Prog3
    {
        static void Main(string[] args)
        {
            // This is a Single-line comment
            Console.WriteLine("Hello World!\n");

            Console.WriteLine("Hello World!\n");  // This is a comment at the end of a line of code

            /* (This is a Multi-line Comment (or a comment block)) The code below will print the words Hello World
            to the screen, and it is amazing */
            Console.WriteLine("Hello World!");
        }
    }
}