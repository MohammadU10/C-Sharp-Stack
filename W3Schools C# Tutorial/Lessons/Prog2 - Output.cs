namespace Prog2___Output
{
    class Prog2
    {
        static void Main(string[] args)
        {
            // Use WriteLine() to output values, text, and numbers
            // Note that it will add a new line for each WriteLine() method:
            Console.WriteLine("Hello World!");
            Console.WriteLine("I am Learning C#");
            Console.WriteLine("It is awesome!");
            Console.WriteLine(3 + 3);

            // Write() Method is similar to WriteLine() Method
            // The only difference is that it does not insert a new line at the end of the output, it will print on the same line:
            Console.Write("Hello World! ");
            Console.Write("I will print on the same line.");
        }
    }
}